package com.bbstudios.hks;

public class sanawucin {
    String id,zaga,duzum;
    public sanawucin(String id, String zaga, String duzum) {
        this.id = id;
        this.zaga = zaga;
        this.duzum = duzum;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getZaga() {
        return zaga;
    }

    public void setZaga(String zaga) {
        this.zaga = zaga;
    }

    public String getDuzum() {
        return duzum;
    }

    public void setDuzum(String duzum) {
        this.duzum = duzum;
    }
}
